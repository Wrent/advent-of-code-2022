package cz.wrent.advent

fun main() {
	val result = partOne()
	println("2a: $result")
	val resultTwo = partTwo()
	println("2b: $resultTwo")
}

private fun partOne(): Int {
	return input.split("\n").map {
		val split = it.split(" ")
		split.get(0).toSymbol() to split.get(1).toPartOneResponse()
	}
		.map { evaluateGame(it.first, it.second) + it.second.points }
		.sum()
}

private fun partTwo(): Int {
	return input.split("\n").map {
		val split = it.split(" ")
		val their = split.get(0).toSymbol()
		val mine = when (split.get(1)) {
			"X" -> symbolBeats[their]!!
			"Y" -> their
			"Z" -> symbolBeats.entries.find { it.value == their }!!.key
			else -> error("fail")
		}
		their to mine
	}
		.map { evaluateGame(it.first, it.second) + it.second.points }
		.sum()
}

enum class Symbol(val value: String, val partOneResponse: String, val points: Int) {
	ROCK("A", "X", 1),
	PAPER("B", "Y", 2),
	SCISSORS("C", "Z", 3)
}

private val symbolBeats = mapOf<Symbol, Symbol>(
	Symbol.ROCK to Symbol.SCISSORS,
	Symbol.PAPER to Symbol.ROCK,
	Symbol.SCISSORS to Symbol.PAPER,
)

private fun String.toSymbol() =
	Symbol.values().find { it.value == this } ?: error(this)

private fun String.toPartOneResponse() =
	Symbol.values().find { it.partOneResponse == this } ?: error(this)

private fun evaluateGame(their: Symbol, mine: Symbol): Int {
	if (their == mine) return 3
	val beats = symbolBeats.get(their)!!
	if (beats == mine) return 0
	else return 6
}

